static void Set2w(IntPt &point, const double x1, const double x2, const double w)
{
  point.pt[0] = x1;
  point.pt[1] = x2;
  point.weight = w;
}

static void AddTriMidPoint(IntPt *quadPoints, const int off, const double weight)
{
  Set2w(quadPoints[off], 1. / 3., 1. / 3., weight);
}

static void AddTriPoints3(IntPt *quadPoints, const int off, const double a, const double b,
                          const double weight)
{
  Set2w(quadPoints[off + 0], a, a, weight);
  Set2w(quadPoints[off + 1], a, b, weight);
  Set2w(quadPoints[off + 2], b, a, weight);
}

static void AddTriPoints3(IntPt *quadPoints, const int off, const double a, const double weight)
{
  AddTriPoints3(quadPoints, off, a, 1. - 2. * a, weight);
}

static void AddTriPoints3b(IntPt *quadPoints, const int off, const double b, const double weight)
{
  AddTriPoints3(quadPoints, off, (1. - b) / 2., b, weight);
}

